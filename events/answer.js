
let colorCircle = document.getElementsByClassName("circle");

function changeRed() {
  colorCircle[0].style.backgroundColor = 'red';
}

function changeBlue() {
  colorCircle[0].style.backgroundColor = 'blue';
}

function changeGreen() {
  colorCircle[0].style.backgroundColor = 'green';
}

function changeTeal() {
  colorCircle[0].style.backgroundColor = 'teal';
}


